package testApp;

import static org.junit.jupiter.api.Assertions.*;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import App.GPS;
import javafx.util.Pair;

public class testGPS {
	private Pair<Integer,Integer> posicion;
	private GPS gps;
	
	@BeforeEach
	public void setUp() throws Exception {	
		posicion = new Pair<Integer, Integer>(1, 2);
		gps = new GPS(posicion);
	}
	
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//// ZONA TEST 	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	public void testGetPosicion() {
		assertEquals(posicion, gps.getPosicion());
	}
	
	public void testSetPosicion() {
		gps.setPosicion(new Pair<>(6, 4));
		assertNotEquals(posicion, gps.getPosicion());
	}
	
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//// MAIN TESTS /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	@Test
	void maintestGetPosicion(){
		testGetPosicion();
	}
	
	@Test
	void mainTestSetPosicion() {
		testSetPosicion();
	}
}
